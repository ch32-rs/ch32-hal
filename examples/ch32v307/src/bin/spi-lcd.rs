//! ST7735 LCD example
//! This example is a simple version of https://github.com/Yttehs-HDX/CH32-ST7735-Demo
//! 
//! Following pins are used:
//! On board:
//! - PA14 - KEY
//! ST7735 to board:
//! GND - GND
//! VCC - 3V3
//! SCL - PB3
//! SDA - PB5
//! RES - PB4
//! DC - PD3
//! CS - PD4
//! BLK - PA15
//! 
//! For this example, you need to customize the constants below:
//! [DISPLAY_WIDTH]
//! [DISPLAY_HEIGHT]
//! [DISPLAY_RGB]
//! [DISPLAY_INVERTED]
//! 
//! Button 'USER' is used to change the brightness of the backlight.

#![no_std]
#![no_main]
#![feature(impl_trait_in_assoc_type)]

use ch32_hal::{
    gpio::{Input, Level, Output, Pin, Pull},
    println,
    spi::Spi,
    time::Hertz,
    timer::{
        low_level::CountingMode,
        simple_pwm::{PwmPin, SimplePwm},
        Channel,
    },
};
use embassy_executor::Spawner;
use embassy_time::{Delay, Timer};
use embedded_graphics::{
    draw_target::DrawTarget,
    image::{ImageDrawable, ImageRaw},
    pixelcolor::{raw::LittleEndian, Rgb565},
    prelude::RgbColor,
};
use panic_halt as _;

const DISPLAY_WIDTH: u16 = 144;
const DISPLAY_HEIGHT: u16 = 144;
const DISPLAY_RGB: bool = false;
const DISPLAY_INVERTED: bool = false;

#[embassy_executor::main(entry = "qingke_rt::entry")]
async fn main(_spawner: Spawner) -> ! {
    ch32_hal::debug::SDIPrint::enable();
    let p = ch32_hal::init(ch32_hal::Config::default());

    let (dc, rst, cs) = (p.PD3, p.PB4, p.PD4);
    let (sck, sda) = (p.PB3, p.PB5);
    let blk = p.PA15;
    let spi = Spi::new_blocking_txonly(p.SPI3, sck, sda, Default::default());
    let dc = Output::new(dc.degrade(), Level::Low, Default::default());
    let rst = Output::new(rst.degrade(), Level::Low, Default::default());
    let cs = Output::new(cs.degrade(), Level::Low, Default::default());
    let spi_device = embedded_hal_bus::spi::ExclusiveDevice::new_no_delay(spi, cs).unwrap();

    let mut display = st7735_lcd::ST7735::new(
        spi_device,
        dc,
        rst,
        DISPLAY_RGB,
        DISPLAY_INVERTED,
        DISPLAY_WIDTH as u32,
        DISPLAY_HEIGHT as u32,
    );
    display.init(&mut Delay).unwrap();

    let mut pwm = SimplePwm::new(
        p.TIM2,
        Some(PwmPin::new_ch1::<1>(blk)),
        None,
        None,
        None,
        Hertz::khz(1),
        CountingMode::default(),
    );
    let ch1 = Channel::Ch1;
    let max_duty = pwm.get_max_duty();

    let button = Input::new(p.PA0, Pull::Up);

    display
        .set_orientation(&st7735_lcd::Orientation::Landscape)
        .unwrap();
    display.set_offset(0, 0);
    display.clear(Rgb565::BLACK).unwrap();
    display.set_offset(
        (DISPLAY_WIDTH - IMAGE_WIDTH) / 2,
        (DISPLAY_HEIGHT - IMAGE_HEIGHT) / 2,
    );
    let image: ImageRaw<Rgb565, LittleEndian> = ImageRaw::new(IMAGE, IMAGE_WIDTH as u32);
    image.draw(&mut display).unwrap();

    let mut bright = Bright(10);
    pwm.set_duty(ch1, max_duty);
    pwm.enable(ch1);
    loop {
        let (mut flag1, mut flag2) = (false, false);
        if button.is_low() {
            flag1 = true;
        }
        Timer::after_millis(10).await;
        if button.is_high() {
            flag2 = true;
        }

        if flag1 && flag2 {
            bright = bright.next();
            pwm.set_duty(ch1, max_duty / 10 * (bright.0 as u32));
            println!("current level: {:?}\r", bright.0);
        }
    }
}

struct Bright(u8);

impl Bright {
    fn next(&self) -> Self {
        let next = (self.0 + 1) % 11;
        Self(next)
    }
}

